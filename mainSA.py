    print("Step 2: Building the definitive model...")
    l2_reg = 0.001

    model = Sequential([
        Input(shape=input_shape),
        Conv2D(64, (3, 3), activation='relu', padding='same', kernel_regularizer=l2(l2_reg)),
        BatchNormalization(),
        MaxPooling2D((3, 3), strides=(2, 2), padding='same'),
        Dropout(0.3),

        Conv2D(128, (3, 3), activation='relu', padding='same', kernel_regularizer=l2(l2_reg)),
        BatchNormalization(),
        MaxPooling2D((3, 3), strides=(2, 2), padding='same'),
        Dropout(0.3),

        Conv2D(256, (3, 3), activation='relu', padding='same', kernel_regularizer=l2(l2_reg)),
        BatchNormalization(),
        MaxPooling2D((3, 3), strides=(2, 2), padding='same'),
        Dropout(0.3),

        Reshape((32, 16 * 256)),

        Bidirectional(LSTM(128, return_sequences=True)),
        Attention(),

        Dense(128, activation='relu', kernel_regularizer=l2(l2_reg)),
        BatchNormalization(),
        Dropout(0.5),

        Dense(len(EMOTION_LABELS), activation='softmax')
    ])

