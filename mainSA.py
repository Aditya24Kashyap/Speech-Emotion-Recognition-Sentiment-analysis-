# --- 2. DATA AUGMENTATION ---
def augment_data(audio, sr=SAMPLE_RATE):
    if np.random.rand() < 0.6:
        noise_factor = 0.05 * np.random.uniform() * np.amax(audio)
        return audio + noise_factor * np.random.randn(len(audio))
    else:
        stretch_rate = np.random.uniform(0.7, 1.3)
        stretched_audio = librosa.effects.time_stretch(y=audio, rate=stretch_rate)
        pitch_shift_steps = np.random.uniform(-4, 4)
        return librosa.effects.pitch_shift(y=stretched_audio, sr=sr, n_steps=pitch_shift_steps)

# --- 3. DATA LOADING ---
def load_data():
    X, y = [], []
    if not os.path.exists(RAVDESS_PATH) or not any(os.scandir(RAVDESS_PATH)):
        print(f"RAVDESS_PATH '{RAVDESS_PATH}' is empty or does not exist.")
        return np.array([]), np.array([])

    for dirname, _, filenames in os.walk(RAVDESS_PATH):
        for filename in sorted(filenames):
            if filename.endswith('.wav'):
                try:
                    emotion_code = filename.split('-')[2]
                    if emotion_code in EMOTION_LABELS:
                        file_path = os.path.join(dirname, filename)

                        spectrogram = extract_spectrogram(file_path, augment=False)
                        if spectrogram is not None:
                            X.append(spectrogram)
                            y.append(EMOTION_LABELS[emotion_code])

                        # Create 3 augmented versions
                        for _ in range(3):
                            spectrogram_aug = extract_spectrogram(file_path, augment=True)
                            if spectrogram_aug is not None:
                                X.append(spectrogram_aug)
                                y.append(EMOTION_LABELS[emotion_code])
                except IndexError:
                    print(f"Skipping file with unexpected format: {filename}")
    return np.array(X), np.array(y)

def extract_spectrogram(file_path, augment=False):
    try:
        # Load audio, ensuring it's not too long to avoid memory issues
        audio, sample_rate = librosa.load(file_path, sr=SAMPLE_RATE, duration=3.5, offset=0.5)
        if augment:
            audio = augment_data(audio, sr=sample_rate)

        spectrogram = librosa.feature.melspectrogram(y=audio, sr=sample_rate, n_mels=N_MELS, n_fft=N_FFT, hop_length=HOP_LENGTH)
        spectrogram_db = librosa.power_to_db(spectrogram, ref=np.max)

        if spectrogram_db.shape[1] > FIXED_WIDTH:
            spectrogram_db = spectrogram_db[:, :FIXED_WIDTH]
        else:
            spectrogram_db = np.pad(spectrogram_db, ((0, 0), (0, FIXED_WIDTH - spectrogram_db.shape[1])), mode='constant')

        return spectrogram_db[..., np.newaxis]
    except Exception as e:
        print(f"Error processing {file_path}: {e}")
        return None

# --- 4. ATTENTION LAYER ---
class Attention(Layer):
    def __init__(self, **kwargs):
        super(Attention, self).__init__(**kwargs)

    def build(self, input_shape):
        self.W = self.add_weight(name="att_weight", shape=(input_shape[-1], 1), initializer="glorot_uniform")
        self.b = self.add_weight(name="att_bias", shape=(input_shape[1], 1), initializer="zeros")
        super(Attention, self).build(input_shape)

    def call(self, x):
        et = K.squeeze(K.tanh(K.dot(x, self.W) + self.b), axis=-1)
        at = K.softmax(et)
        at = K.expand_dims(at, axis=-1)
        output = x * at
        return K.sum(output, axis=1)

    def compute_output_shape(self, input_shape):
        return (input_shape[0], input_shape[-1])

