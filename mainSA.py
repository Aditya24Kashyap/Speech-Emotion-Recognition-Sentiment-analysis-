def find_example_files(base_path, emotion_map):
    """Finds one file for each emotion."""
    emotion_files = {label: None for label in emotion_map.values()}
    emotions_needed = list(emotion_map.keys())

    if not os.path.exists(base_path):
        print(f"Error: Path does not exist: {base_path}")
        return {}

    for dirname, _, filenames in os.walk(base_path):
        for filename in sorted(filenames):
            if not filename.endswith('.wav'):
                continue

            try:
                parts = filename.split('-')
                emotion_code = parts[2]

                if emotion_code in emotions_needed:
                    label_name = emotion_map[emotion_code]
                    if emotion_files[label_name] is None:
                        emotion_files[label_name] = os.path.join(dirname, filename)
                        emotions_needed.remove(emotion_code)
                        print(f"Found '{label_name}' sample: {filename}")

                if not emotions_needed:
                    return emotion_files

            except IndexError:
                continue

    return emotion_files

def get_audio_and_spectrogram(file_path):
    """Loads audio, creates a waveform, AND creates a log-power Mel spectrogram."""
    try:
        audio, sr = librosa.load(file_path, sr=SAMPLE_RATE)

        spectrogram = librosa.feature.melspectrogram(
            y=audio,
            sr=sr,
            n_mels=N_MELS,
            n_fft=N_FFT,
            hop_length=HOP_LENGTH
        )
        spectrogram_db = librosa.power_to_db(spectrogram, ref=np.max)

        # Return all components
        return audio, sr, spectrogram_db

    except Exception as e:
        print(f"Error processing {file_path}: {e}")
        return None, None, None

